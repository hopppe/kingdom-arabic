import json

# Read the file
with open('/Users/ethanhoppe/Documents/Cursor_Code/LearnArabic/bible-maps-word-haiku/mappings/GEN/6.json', 'r', encoding='utf-8') as f:
    data = json.load(f)

# Apply translations
total_translated = 0
for verse_num in data["verses"]:
    for mapping in data["verses"][verse_num]["mappings"]:
        ar_word = mapping["ar"]
        if mapping["en"] == "" and ar_word in translations:
            mapping["en"] = translations[ar_word]
            total_translated += 1
            print(f"Verse {verse_num}: {ar_word} -> {translations[ar_word]}")

# Save the file
with open('/Users/ethanhoppe/Documents/Cursor_Code/LearnArabic/bible-maps-word-haiku/mappings/GEN/6.json', 'w', encoding='utf-8') as f:
    json.dump(data, f, ensure_ascii=False, indent=2)

print(f"\n✓ Translated {total_translated} words")

# Check remaining empty translations
remaining = 0
for verse_num in data["verses"]:
    for mapping in data["verses"][verse_num]["mappings"]:
        if mapping["en"] == "":
            remaining += 1
            print(f"Remaining in verse {verse_num}: {mapping['ar']}")

print(f"\nRemaining empty: {remaining}")
